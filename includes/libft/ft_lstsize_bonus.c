#include "libft.h"

int	ft_lstsize(t_lst *lst)
{
	t_lst	*tmp;
	int		i;	

	i = 0;
	tmp = lst;
	while (tmp)
	{
		tmp = tmp->next;
		i++;
	}
	return (i);
}
